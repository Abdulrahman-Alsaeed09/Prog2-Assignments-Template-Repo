#include <iostream>
using namespace std;
int main() {
	long long x1, x2;
	cin >>x1 >> x2;
	cout << x1 << " + " << x2 << " = " << x1 + x2 << endl;
	cout << x1 << " * " << x2 << " = " << x1 * x2 << endl;
	cout << x1 << " - " << x2 << " = " << x1 - x2 << endl;
	return 0;
}